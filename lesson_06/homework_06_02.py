'''Напишіть цикл, який буде вимагати від користувача ввести слово, в якому є літера "h"
(враховуються як великі так і маленькі).
Цикл не повинен завершитися, якщо користувач ввів слово без букви "h".'''

char_h_H = 0
while char_h_H != 1:
    print('Введіть слово з літерою h або H:')
    snt = input()
    for char in snt:
        if char == 'h' or char == 'H':
            char_h_H += 1
            print('користувач ввів слово, в якому є літера', char)
            break
